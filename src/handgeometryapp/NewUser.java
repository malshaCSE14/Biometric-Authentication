        float[] widthArray = {Float.parseFloat(jTextField9.getText()),Float.parseFloat(jTextField10.getText()),Float.parseFloat(jTextField11.getText()),Float.parseFloat(jTextField12.getText()),Float.parseFloat(jTextField13.getText())}; 
