       User newUser = new User();
       newUser.setName(jTextField1.getText());
       newUser.setLengthArray(lengthArray);
       newUser.setTipToDivisionArray(tipToDivisionArray);
       newUser.setWidthArray(widthArray);
       IOhandling io = new IOhandling();
       io.saveUser(newUser);
       this.dispose();
